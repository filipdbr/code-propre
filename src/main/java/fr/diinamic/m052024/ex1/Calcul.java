package fr.diinamic.m052024.ex1;

/**
 * La classe Calcul fournit des méthodes qui effectuent
 * des opérations mathématiques telles que l'addition et la soustraction.
 */
public class Calcul {

	/**
	 * La fonction addition calcule la somme de 2 entiers
	 *
	 * @param a est le premier entier défini par l'utilisateur
	 * @param b est le deuxième entier défini par l'utilisateur
	 * @return la somme des deux chiffres
	 */
	public int addition(int a, int b){
		return a+b;
	}

	/**
	 * La fonction calcule la différence de 2 entiers
	 *
	 * @param a est le premier entier défini par l'utilisateur
	 * @param b est le deuxième entier défini par l'utilisateur
	 * @return la différence des deux chiffres
	 */
	public int soustraction(int a, int b){
		return a-b;
	}
}
